#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	i;
	long	num;

	num = (long)n;
	if (num < 0)
	{
		write(fd, "-", 1);
		num *= -1;
	}
	if (num >= 10)
		ft_putnbr_fd(num / 10, fd);
	i = (num % 10) + '0';
	write(fd, &i, 1);
}
