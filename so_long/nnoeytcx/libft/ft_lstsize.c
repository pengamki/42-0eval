#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	n;

	n = 1;
	if (!lst)
		return (0);
	while (lst -> next != NULL)
	{
		n++;
		lst = lst -> next;
	}
	return (n);
}
