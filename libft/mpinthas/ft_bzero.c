#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*ss;
	size_t	i;

	ss = (char *)s;
	i = 0;
	while (n > 0)
	{
		ss[i] = 0;
		i++;
		n--;
	}
}
