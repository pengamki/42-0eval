#include "libft.h"

int	ft_isalnum(int c)
{
	int	n;

	n = 0;
	if ((c >= '0' && c <= '9') || \
			(c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
	{
		n = 8;
	}
	return (n);
}
