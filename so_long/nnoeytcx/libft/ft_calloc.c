#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*p;

	p = (void *)malloc(count * size);
	if (!p || count == SIZE_MAX || size == SIZE_MAX)
		return (NULL);
	ft_bzero(p, count * size);
	return (p);
}
